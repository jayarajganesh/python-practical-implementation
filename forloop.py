#1. Iterating Over a List

fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

#2. Using range()
for num in range(5):
    print(num)

#3. Iterating Over a String
word = "jayaraj"
for char in word:
    print(char)

#4. Iterating Over a Dictionary
person = {"name": "John", "age": 25, "city": "New York"}
for key, value in person.items():
    print(f"Key: {key}, Value: {value}")