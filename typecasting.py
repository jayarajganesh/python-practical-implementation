#1. Integer to Float
num = 10  # Integer
converted = float(num)
print(converted)

#2. Float to Integer
num = 3.14  # Float
converted = int(num)  # Convert to integer (truncates decimal part)
print(converted)

#3. String to Integer
num_str = "123"  # String
converted = int(num_str)  # Convert to integer
print(converted + 1)
